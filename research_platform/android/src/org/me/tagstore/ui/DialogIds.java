package org.me.tagstore.ui;

/**
 * This class stores the dialog identifiers for all dialogs
 * @author Johannes Anderwald
 *
 */
public class DialogIds {

	/**
	 * general file dialog id
	 */
	public static final int DIALOG_GENERAL_FILE_MENU = 1;
	
	/**
	 * launches the details view
	 */
	public static final int DIALOG_DETAILS = 2;

	/**
	 * launches the rename dialog
	 */
	public static final int DIALOG_RENAME = 3;

	/**
	 * retag dialog id
	 */
	public static final int DIALOG_RETAG = 4;
	
	/**
	 * general tag menu
	 */
	public static final int DIALOG_GENERAL_TAG_MENU = 5;

	/**
	 * tag details dialog
	 */
	public static final int DIALOG_DETAILS_TAG = 6;
	
	/**
	 * stores the dialog id of options dialog
	 */
	public static final int DIALOG_OPTIONS = 7;

	/**
	 * stores the dialog id of the open as dialog
	 */
	public static final int DIALOG_OPEN_AS = 8;
}
